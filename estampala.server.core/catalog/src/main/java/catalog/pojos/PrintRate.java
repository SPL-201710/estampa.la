package catalog.pojos;

public class PrintRate {

	private String userId;
	
	private Float rate;
	
	public PrintRate() {
		
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Float getRate() {
		return rate;
	}

	public void setRate(Float rate) {
		this.rate = rate;
	}
}
